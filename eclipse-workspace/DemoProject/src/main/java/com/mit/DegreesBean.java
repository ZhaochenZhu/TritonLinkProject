package com.mit;

public class DegreesBean {
    String major;
    String type;
    String category;
    int minimum_unit;
    String minimum_grade;

    public String getMajor() {
        return this.major;
    }

    public void setMajor(String major) {
        this.major = major;
    }

    public String getType() {
        return this.type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
    public String getCategory() {
        return this.category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public int getMinimum_unit() {
        return this.minimum_unit;
    }

    public void setMinimum_unit(int minimum_unit) {
        this.minimum_unit = minimum_unit;
    }

    public String getMinimum_grade() {
        return this.minimum_grade;
    }

    public void setMinimum_grade(String minimum_grade) {
        this.minimum_grade = minimum_grade;
    }
    
    
    
}
