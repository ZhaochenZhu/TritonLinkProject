package com.mit;

public class StudentBean {
	int student_id;
	String first_name;
	String last_name;
	String middle_name;
	int ssn;
	String enrolled;
	String residential_status;
	String current_degree;
	
	public int getStudent_id() {
		return student_id;
	}
	public void setStudent_id(int student_id) {
		this.student_id = student_id;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	public String getMiddle_name() {
		return middle_name;
	}
	public void setMiddle_name(String middle_name) {
		this.middle_name = middle_name;
	}
	public int getSsn() {
		return ssn;
	}
	public void setSsn(int ssn) {
		this.ssn = ssn;
	}
	public String getEnrolled() {
		return enrolled;
	}
	public void setEnrolled(String enrolled) {
		this.enrolled = enrolled;
	}
	public String getResidential_status() {
		return residential_status;
	}
	public void setResidential_status(String residential_status) {
		this.residential_status = residential_status;
	}
	public String getCurrent_degree() {
		return current_degree;
	}
	public void setCurrent_degree(String current_degree) {
		this.current_degree = current_degree;
	}
	
	
}
